# Built são pre-funções do python nativas

# abs() retorna o valor absoluto

abs = abs(-9)

print(abs)

# min() retorna o mínimo de uma sequencia

min = min(6,7,8,9,10)

print(min)

# max() retorna o máximo de uma sequencia

max = max(6,7,8,9,10)

print(max)

# sum() soma de uma lista em sequencia

soma = sum([1, 2, 3])

print(soma)

# pow() exponenciação

elevado = 2**3

print(elevado)

pow = pow(2,3)

print(pow)

# input() lêr uma valor digitado pela interação do usuário pelo teclado

nome = input('Qual o seu nome?')

print(nome)

# help() descrição de declarações do python

# help(sum)

print(len(nome))